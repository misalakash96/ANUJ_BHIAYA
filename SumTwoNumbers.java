public class SumTwoNumbers {
    public static void main(String[] args) {
        // Declare and initialize the two numbers
        int a = 5;
        int b = 7;

        // Calculate the sum
        int sum = a + b;

        // Display the result
        System.out.println("The sum of given numbers" + a + " and " + b + " is: " + sum);
    }
}
